"""Every value in Python has a datatype.
Since everything is an object in Python,
data types are actually classes and variables are
instances (objects) of these classes."""

#common assignment
a = 5
b = 3.2
c = "Hello"
print(a, b, c)

#multiple assignments, each value for a variable
a, b, c = 50, 3.22, "Helloo"
print(a, b, c)

#multiple assignments, unique value for all variables
x = y = z = "same value"
print(x, y, z)